package com.leimingtech.mobile.entity.appapi.contentstatistics;

import com.wordnik.swagger.annotations.ApiModel;
import com.wordnik.swagger.annotations.ApiModelProperty;

/**
 * Created by hejiping on 2017/5/23.
 */
@ApiModel
public class ContentStatisticsEntity {

    @ApiModelProperty(value = "内容编号",required = true)
    private String contentId;
    @ApiModelProperty(value = "频道编号",required = true)
    private String channelId;
    @ApiModelProperty(value = "访问者",required = true)
    private String visitUser;
    @ApiModelProperty(value = "终端类型",required = true)
    private String terminalType;//如：安卓
    @ApiModelProperty(value = "手机型号",required = true)
    private String mobileModel;//如：三星s7
    @ApiModelProperty(value = "终端系统版本",required = true)
    private String terminalSystemVersion;//如：安卓5.0
    @ApiModelProperty(value = "运行商",required = true)
    private String operator;
    @ApiModelProperty(value = "网络",required = true)
    private String network;
    @ApiModelProperty(value = "app应用版本",required = true)
    private String appApplicationVersion;

    public String getContentId() {
        return contentId;
    }

    public void setContentId(String contentId) {
        this.contentId = contentId;
    }

    public String getChannelId() {
        return channelId;
    }

    public void setChannelId(String channelId) {
        this.channelId = channelId;
    }

    public String getVisitUser() {
        return visitUser;
    }

    public void setVisitUser(String visitUser) {
        this.visitUser = visitUser;
    }

    public String getTerminalType() {
        return terminalType;
    }

    public void setTerminalType(String terminalType) {
        this.terminalType = terminalType;
    }

    public String getMobileModel() {
        return mobileModel;
    }

    public void setMobileModel(String mobileModel) {
        this.mobileModel = mobileModel;
    }

    public String getTerminalSystemVersion() {
        return terminalSystemVersion;
    }

    public void setTerminalSystemVersion(String terminalSystemVersion) {
        this.terminalSystemVersion = terminalSystemVersion;
    }

    public String getOperator() {
        return operator;
    }

    public void setOperator(String operator) {
        this.operator = operator;
    }

    public String getNetwork() {
        return network;
    }

    public void setNetwork(String network) {
        this.network = network;
    }

    public String getAppApplicationVersion() {
        return appApplicationVersion;
    }

    public void setAppApplicationVersion(String appApplicationVersion) {
        this.appApplicationVersion = appApplicationVersion;
    }
}
