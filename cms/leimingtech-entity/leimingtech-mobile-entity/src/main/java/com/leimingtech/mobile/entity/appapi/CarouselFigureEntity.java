package com.leimingtech.mobile.entity.appapi;

import com.wordnik.swagger.annotations.ApiModel;
import com.wordnik.swagger.annotations.ApiModelProperty;

/**
 * Created by hejiping on 2017/5/21.
 */
@ApiModel
public class CarouselFigureEntity {

    @ApiModelProperty(value="内容编号",required = true)
    private String contentId;
    @ApiModelProperty(value="内容类型")
    private String contentType;
    @ApiModelProperty(value="图片路径")
    private String imgUrl;
    @ApiModelProperty(value="图片名称")
    private String imgTitile;
    @ApiModelProperty(value="图片描述")
    private String imgDesc;

    public String getContentId() {
        return contentId;
    }

    public void setContentId(String contentId) {
        this.contentId = contentId;
    }

    public String getContentType() {
        return contentType;
    }

    public void setContentType(String contentType) {
        this.contentType = contentType;
    }

    public String getImgUrl() {
        return imgUrl;
    }

    public void setImgUrl(String imgUrl) {
        this.imgUrl = imgUrl;
    }

    public String getImgTitile() {
        return imgTitile;
    }

    public void setImgTitile(String imgTitile) {
        this.imgTitile = imgTitile;
    }

    public String getImgDesc() {
        return imgDesc;
    }

    public void setImgDesc(String imgDesc) {
        this.imgDesc = imgDesc;
    }
}
